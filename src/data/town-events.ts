/**
 * Curated happenings & time-pass plans (town script layer).
 * Mapped to Place + category "event" so existing UI sections pick them up.
 */

type TownEventSeed = Omit<Place, "city" | "distance"> & { city: SupportedCityName };

const withCityDistance = (seed: TownEventSeed): Place => {
  const center = CITY_CENTERS[seed.city];
  return {
    ...seed,
    image: resolvePlaceImage({
      id: seed.id,
      title: seed.title,
      category: seed.category,
      city: seed.city,
    }),
    distance: Math.round(calculateDistance(center.latitude, center.longitude, seed.latitude, seed.longitude) * 10) / 10,
  };
};

