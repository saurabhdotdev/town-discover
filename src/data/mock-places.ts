export const MOCK_USER_LOCATION = PUNE_CENTER;

export const calculateDistance = (
  lat1: number,
  lon1: number,
  lat2: number,
  lon2: number
): number => {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLon = ((lon2 - lon1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLon / 2) *
      Math.sin(dLon / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
};

const distanceFromPune = (latitude: number, longitude: number): number => {
  return Math.round(calculateDistance(PUNE_CENTER.latitude, PUNE_CENTER.longitude, latitude, longitude) * 10) / 10;
};

const distanceFromCity = (city: SupportedCityName, latitude: number, longitude: number): number => {
  const center = CITY_CENTERS[city];
  return Math.round(calculateDistance(center.latitude, center.longitude, latitude, longitude) * 10) / 10;
};

const punePlace = (place: Omit<Place, "city" | "distance">): Place => ({
  ...place,
  city: "Pune",
  distance: distanceFromPune(place.latitude, place.longitude),
});

const cityPlace = (city: SupportedCityName, place: Omit<Place, "city" | "distance">): Place => ({
  ...place,
  city,
  distance: distanceFromCity(city, place.latitude, place.longitude),
});

