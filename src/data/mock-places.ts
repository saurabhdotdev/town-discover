// Generate and push all scaled cities places into the MOCK_PLACES array
Object.entries(SCALED_CITIES_TEMPLATES).forEach(([city, templates]) => {
  templates.forEach((template) => {
    MOCK_PLACES.push(cityPlace(city as SupportedCityName, template));
  });
});


export const getPlacesByCategory = (category: string): Place[] => {
  return MOCK_PLACES.filter((place) => place.category === category);
};

export const getTrendingPlaces = (): Place[] => {
  return MOCK_PLACES.filter((place) => place.isTrending).sort((a, b) => b.rating - a.rating);
};

export const getFreeShortPlans = (): Place[] => {
  return MOCK_PLACES.filter(
    (place) => place.tags.includes("free") && place.tags.includes("tourist-friendly")
  ).sort((a, b) => {
    if (a.isTrending !== b.isTrending) return Number(b.isTrending) - Number(a.isTrending);
    return a.distance - b.distance;
  });
};

export const getNearbyPlaces = (maxDistance: number = 5): Place[] => {
  return MOCK_PLACES.filter((place) => place.distance <= maxDistance).sort((a, b) => a.distance - b.distance);
};


export const getPlacesWithDistance = (
  places: Place[],
  location: UserLocation | null | undefined = PUNE_CENTER
): Place[] => {
  const activeLocation = location ?? PUNE_CENTER;

  return places
    .map((place) => ({
      ...place,
      distance: Math.round(
        calculateDistance(activeLocation.latitude, activeLocation.longitude, place.latitude, place.longitude) * 10
      ) / 10,
    }))
    .sort((a, b) => a.distance - b.distance);
};
