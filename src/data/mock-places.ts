const distanceFromPune = (latitude: number, longitude: number): number => {
  return Math.round(calculateDistance(PUNE_CENTER.latitude, PUNE_CENTER.longitude, latitude, longitude) * 10) / 10;
};

const distanceFromCity = (city: SupportedCityName, latitude: number, longitude: number): number => {
  const center = CITY_CENTERS[city];
  return Math.round(calculateDistance(center.latitude, center.longitude, latitude, longitude) * 10) / 10;
};

const reelSearchUrl = (place: string, city: string) =>
  `https://www.instagram.com/explore/search/keyword/?q=${encodeURIComponent(`${place} ${city} food reel`)}`;

const creatorFeature = (
  creatorName: string,
  handle: string,
  rating: number,
  place: string,
  city: string,
  quote: string
): InfluencerFeature => ({
  creatorName,
  handle,
  rating,
  platform: "instagram",
  videoUrl: reelSearchUrl(place, city),
  quote,
});

const influencerFeaturesFor = (place: string, city: SupportedCityName, vibe: string): InfluencerFeature[] => [
  creatorFeature("CityBite Journal", "@citybitejournal", 4.6, place, city, `Good ${vibe} pick with strong local recall.`),
  creatorFeature("Weekend Plate", "@weekendplate", 4.4, place, city, `Worth saving when you want a tested ${city} stop.`),
];

const creatorFriendlyCategories = new Set<Place["category"]>([
  "cafe",
  "restaurant",
  "dessert",
  "food-stall",
  "street-food",
  "bar",
  "nightlife",
]);

const withCreatorFeatures = (place: Omit<Place, "city" | "distance">, city: SupportedCityName) => {
  if (place.influencerFeatures || !creatorFriendlyCategories.has(place.category)) {
    return place;
  }

  return {
    ...place,
    influencerFeatures: influencerFeaturesFor(place.title, city, getCategoryVibe(place.category)),
  };
};

const getCategoryVibe = (category: Place["category"]) => {
  const vibes: Record<Place["category"], string> = {
    cafe: "cafe",
    restaurant: "food",
    event: "city",
    nightlife: "nightlife",
    "food-stall": "street food",
    bar: "bar",
    dessert: "dessert",
    "ice-cream": "ice cream",
    "street-food": "street food",
  };

  return vibes[category];
};

const withLocalImage = (place: Omit<Place, "city" | "distance">, city: SupportedCityName) => ({
  ...place,
  image: resolvePlaceImage({
    id: place.id,
    title: place.title,
    category: place.category,
    city,
  }),
});

export const enrichTags = (title: string, category: Place["category"], existingTags: string[]): string[] => {
  const nameLen = title.length;
  const tagsSet = new Set(existingTags);

  // pet-friendly: 20% cafe/restaurant/bar, 100% parks/gardens/etc.
  if (nameLen % 5 === 0 || tagsSet.has("park") || tagsSet.has("garden") || tagsSet.has("outdoor") || tagsSet.has("pet-friendly")) {
    tagsSet.add("pet-friendly");
  }
  // family-friendly: 25% restaurants/desserts, 100% kid parks/playgrounds/etc.
  if (nameLen % 4 === 0 || category === "restaurant" || tagsSet.has("family") || tagsSet.has("family-friendly")) {
    tagsSet.add("family-friendly");
  }
  // work-friendly: 15% cafes/wifi
  if (nameLen % 6 === 0 || tagsSet.has("wifi") || tagsSet.has("specialty-coffee") || tagsSet.has("work-friendly")) {
    tagsSet.add("work-friendly");
  }
  // heritage: historic tags
  if (tagsSet.has("fort") || tagsSet.has("historic") || tagsSet.has("museum") || tagsSet.has("heritage") || tagsSet.has("temple")) {
    tagsSet.add("heritage");
  }
  // scenic: sunset views, etc.
  if (nameLen % 7 === 0 || tagsSet.has("sunset") || tagsSet.has("lake") || tagsSet.has("sea-face") || tagsSet.has("view") || tagsSet.has("scenic")) {
    tagsSet.add("scenic");
  }
  // live-music: bar/pub
  if (nameLen % 8 === 0 || category === "bar" || tagsSet.has("live") || tagsSet.has("brewpub") || tagsSet.has("live-music")) {
    tagsSet.add("live-music");
  }
  // artisanal: specialty
  if (nameLen % 9 === 0 || tagsSet.has("specialty") || tagsSet.has("organic") || tagsSet.has("brewery") || tagsSet.has("artisanal")) {
    tagsSet.add("artisanal");
  }
  // late-night: bars, nightlife, or 30% cafes
  if (nameLen % 3 === 0 || category === "bar" || category === "nightlife" || tagsSet.has("night-drive") || tagsSet.has("late-night")) {
    tagsSet.add("late-night");
  }

  return Array.from(tagsSet);
};

const punePlace = (place: Omit<Place, "city" | "distance">): Place => {
  const enrichedTags = enrichTags(place.title, place.category, place.tags || []);
  const placeWithTags = { ...place, tags: enrichedTags };
  const enriched = withLocalImage(placeWithTags, "Pune");
  return {
    ...withCreatorFeatures(enriched, "Pune"),
    city: "Pune",
    distance: distanceFromPune(place.latitude, place.longitude),
  };
};

const cityPlace = (city: SupportedCityName, place: Omit<Place, "city" | "distance">): Place => {
  const enrichedTags = enrichTags(place.title, place.category, place.tags || []);
  const placeWithTags = { ...place, tags: enrichedTags };
  const enriched = withLocalImage(placeWithTags, city);
  return {
    ...withCreatorFeatures(enriched, city),
    city,
    distance: distanceFromCity(city, place.latitude, place.longitude),
  };
};

